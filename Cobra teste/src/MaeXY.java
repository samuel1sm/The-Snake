abstract class MaeXY {
    protected int x[]=new int[300],y[]=new int[300];

    public int getX() {
        return x[0];
    }

    public void setX(int x) {
        this.x[0] = x;
    }

    public int getY() {
        return y[0];
    }

    public void setY(int y) {
        this.y[0] = y;
    }
}
